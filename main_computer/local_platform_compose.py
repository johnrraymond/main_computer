def _site_env_prefix(site_id: str) -> str:
    return re.sub(r"[^A-Z0-9]+", "_", str(site_id or "").upper()).strip("_") or "SITE"


def _directus_port_range() -> tuple[int, int]:
    start = _env_port(ENV_DIRECTUS_HOST_PORT_START, DIRECTUS_HOST_PORT_START)
    end = _env_port(ENV_DIRECTUS_HOST_PORT_END, DIRECTUS_HOST_PORT_END)
    if start > end:
        raise LocalPlatformComposeError(
            f"{ENV_DIRECTUS_HOST_PORT_START} must be less than or equal to {ENV_DIRECTUS_HOST_PORT_END}."
        )
    return start, end


def _env_port(name: str, default: int) -> int:
    value = str(os.environ.get(name) or "").strip()
    if not value:
        return default
    try:
        port = int(value)
    except ValueError as exc:
        raise LocalPlatformComposeError(f"Environment variable {name} must be an integer TCP port.") from exc
    if port < 1 or port > 65535:
        raise LocalPlatformComposeError(f"Environment variable {name} must be a TCP port in 1-65535.")
    return port


def _host_port_can_bind(port: int) -> bool:
    try:
        clean_port = int(port)
    except (TypeError, ValueError):
        return False
    if clean_port < 1 or clean_port > 65535:
        return False
    with socket.socket(socket.AF_INET, socket.SOCK_STREAM) as sock:
        sock.setsockopt(socket.SOL_SOCKET, socket.SO_REUSEADDR, 0)
        try:
            sock.bind(("127.0.0.1", clean_port))
        except OSError:
            return False
    return True


def _directus_public_port_from_cms(cms: dict[str, Any]) -> int | None:
    connection_port = _directus_local_connection_public_port(cms)
    if connection_port is not None:
        return connection_port

    service = cms.get("service")
    if not isinstance(service, dict):
        return None
    raw_url = str(service.get("public_url") or "").strip()
    if not raw_url:
        return None
    try:
        parsed = urlsplit(raw_url)
    except ValueError as exc:
        raise LocalPlatformComposeError(f"Directus public_url is not a valid URL: {raw_url!r}") from exc
    if parsed.scheme.lower() not in {"http", "https"} or not parsed.hostname or parsed.port is None:
        raise LocalPlatformComposeError(f"Directus public_url must include http(s), host, and port: {raw_url!r}")
    if parsed.port < 1 or parsed.port > 65535:
        raise LocalPlatformComposeError(f"Directus public_url port is outside 1-65535: {raw_url!r}")
    return int(parsed.port)


def _manifest_path_for_site(repo_root: Path, site) -> Path:
    return repo_root / site.repo_relative_path / "site.json"


def _write_runtime_site_manifest(repo_root: Path, site, payload: dict[str, Any]) -> None:
    path = _manifest_path_for_site(repo_root, site)
    path.parent.mkdir(parents=True, exist_ok=True)
    text = json.dumps(payload, indent=2, sort_keys=True) + "\n"
    with tempfile.NamedTemporaryFile("w", encoding="utf-8", dir=path.parent, delete=False) as handle:
        handle.write(text)
        tmp_name = handle.name
    Path(tmp_name).replace(path)


def _persist_directus_urls(repo_root: Path, site, payload: dict[str, Any], *, internal_url: str, public_url: str) -> None:
    backend = payload.setdefault("backend", {})
    if not isinstance(backend, dict):
        backend = {}
        payload["backend"] = backend
    cms = backend.setdefault("cms", {})
    if not isinstance(cms, dict):
        cms = {}
        backend["cms"] = cms
    service = cms.setdefault("service", {})
    if not isinstance(service, dict):
        service = {}
        cms["service"] = service
    changed = False
    if service.get("internal_url") != internal_url:
        service["internal_url"] = internal_url
        changed = True
    if service.get("public_url") != public_url:
        service["public_url"] = public_url
        changed = True
    if changed:
        _write_runtime_site_manifest(repo_root, site, payload)


def _used_manifest_directus_ports(repo_root: Path, registry: LocalPlatformRegistry, *, exclude_site_id: str = "") -> set[int]:
    ports: set[int] = set()
    for other in registry.list_sites():
        if str(other.id) == str(exclude_site_id):
            continue
        manifest = load_runtime_site_manifest(repo_root, other)
        cms = _cms_contract(manifest)
        try:
            port = _directus_public_port_from_cms(cms)
        except LocalPlatformComposeError:
            continue
        if port is not None:
            ports.add(port)
    return ports


def _used_registry_ports(registry: LocalPlatformRegistry) -> set[int]:
    return {lane.port for site in registry.sites.values() for lane in site.lanes.values()}


def _docker_port_owners(port: int) -> list[str]:
    try:
        completed = subprocess.run(
            [
                "docker",
                "ps",
                "-a",
                "--filter",
                f"publish={int(port)}",
                "--format",
                "{{.Names}} {{.Image}} {{.Status}} {{.Ports}}",
            ],
            text=True,
            capture_output=True,
            timeout=4.0,
            check=False,
        )
    except Exception:
        return []
    if completed.returncode != 0:
        return []
    return [line.strip() for line in completed.stdout.splitlines() if line.strip()]


def _compose_service_from_container_name(container_name: str) -> str:
    project_prefix = f"{compose_project_name()}-"
    suffix = "-1"
    if not container_name.startswith(project_prefix) or not container_name.endswith(suffix):
        return ""
    service_name = container_name[len(project_prefix) : -len(suffix)]
    try:
        return validate_compose_service_name(service_name)
    except LocalPlatformComposeError:
        return ""


def _existing_directus_service_for_port(port: int) -> str:
    for owner in _docker_port_owners(port):
        if "directus/" not in owner.lower() and "directus:" not in owner.lower():
            continue
        name = owner.split(" ", 1)[0]
        service = _compose_service_from_container_name(name)
        if service:
            return service
    return ""


def _port_owner_conflict_message(port: int, service_name: str) -> str:
    owners = _docker_port_owners(port)
    expected_name_fragment = f"-{service_name}-"
    expected_suffix = f"-{service_name}-1"
    for owner in owners:
        name = owner.split(" ", 1)[0]
        if expected_name_fragment in name or name.endswith(expected_suffix):
            return ""
    if owners:
        return f"Directus public port {port} is already used by {owners[0]}."
    return f"Directus public port {port} is already used by another local process."


def _validate_persisted_directus_port(port: int, service_name: str) -> int:
    if _host_port_can_bind(port):
        return port
    conflict = _port_owner_conflict_message(port, service_name)
    if conflict:
        raise LocalPlatformComposeError(conflict)
    return port


def _allocate_directus_host_port(repo_root: Path, site, registry: LocalPlatformRegistry) -> int:
    reserved = _used_registry_ports(registry)
    reserved.update(_used_manifest_directus_ports(repo_root, registry, exclude_site_id=site.id))
    start, end = _directus_port_range()
    for port in range(start, end + 1):
        if port in reserved:
            continue
        if _host_port_can_bind(port):
            return port
    raise LocalPlatformComposeError(f"No Directus public port is available in {start}-{end}.")


def _directus_host_port(repo_root: Path, site, cms: dict[str, Any], service_name: str, registry: LocalPlatformRegistry) -> int:
    persisted = _directus_public_port_from_cms(cms)
    if persisted is not None:
        return _validate_persisted_directus_port(persisted, service_name)
    return _allocate_directus_host_port(repo_root, site, registry)


def directus_service_for_site(
    repo_root: Path,
    site,
    registry: LocalPlatformRegistry | None = None,
) -> GeneratedDirectusService | None:
    manifest = load_runtime_site_manifest(repo_root, site)
    manifest = _ensure_directus_contract_for_blog(str(site.id), manifest)
    if not _directus_required(manifest):
        return None
    registry = registry or load_local_platform_registry(repo_root)
    cms = _cms_contract(manifest)
    service_name = _directus_service_host(site.id, cms)
    connection = _directus_local_connection_from_cms(cms)
    persisted_public_port = _directus_public_port_from_cms(cms)
    if (
        _directus_local_connection_mode(cms) == "use_existing"
        and service_name == _directus_default_service_name(site.id)
        and persisted_public_port is not None
    ):
        owner_service = _existing_directus_service_for_port(persisted_public_port)
        if owner_service and owner_service != service_name:
            service_name = owner_service
            connection = {
                **connection,
                "service_name": owner_service,
                "internal_url": f"http://{owner_service}:{DIRECTUS_INTERNAL_PORT}",
                "managed": False,
                "external": True,
            }
    managed = _directus_local_connection_managed(site.id, cms, service_name)
    if connection.get("managed") is False or connection.get("external") is True:
        managed = False
    storage = cms.get("storage")
    storage = storage if isinstance(storage, dict) else {}
    database_volume = _directus_volume(
        _directus_local_connection_volume(cms, "database_volume") or storage.get("database_volume"),
        f"{site.id}_directus_database",
    )
    uploads_volume = _directus_volume(
        _directus_local_connection_volume(cms, "uploads_volume") or storage.get("uploads_volume"),
        f"{site.id}_directus_uploads",
    )
    public_port = _directus_host_port(repo_root, site, cms, service_name, registry) if managed else (
        _directus_public_port_from_cms(cms) or _directus_host_port(repo_root, site, cms, service_name, registry)
    )
    internal_url = str(connection.get("internal_url") or "").strip() or f"http://{service_name}:{DIRECTUS_INTERNAL_PORT}"
    public_url = str(connection.get("public_url") or "").strip() or f"http://127.0.0.1:{public_port}"
    _persist_directus_urls(repo_root, site, manifest, internal_url=internal_url, public_url=public_url)
    prefix = _site_env_prefix(site.id)
    return GeneratedDirectusService(
        site_id=site.id,
        service=service_name,
        image=_directus_image(cms),
        internal_url=internal_url,
        public_url=public_url,
        public_port=public_port,
        database_volume=database_volume,
        uploads_volume=uploads_volume,
        admin_email_env=f"${{{prefix}_DIRECTUS_ADMIN_EMAIL:-admin@example.com}}",
        admin_password_env=f"${{{prefix}_DIRECTUS_ADMIN_PASSWORD:-Admin-password-1!}}",
        secret_env=f"${{{prefix}_DIRECTUS_SECRET:-main-computer-local-{safe_image_slug(site.id)}-directus-secret}}",
        managed=managed,
    )


def generated_directus_services(
    repo_root: Path,
    registry: LocalPlatformRegistry | None = None,
    *,
    directus_site_ids: object = None,
    site_ids: object = None,
) -> list[GeneratedDirectusService]:
    registry = registry or load_local_platform_registry(repo_root)
    directus_scope = _normalize_site_id_scope(directus_site_ids)
    site_scope = _normalize_site_id_scope(site_ids)
    services: list[GeneratedDirectusService] = []
    seen_services: set[str] = set()
    seen_ports: set[int] = set()
    for site in registry.list_sites():
        if site_scope is not None and site.id not in site_scope:
            continue
        if directus_scope is not None and site.id not in directus_scope:
            continue
        directus = directus_service_for_site(repo_root, site, registry)
        if directus is None or not directus.managed:
            continue
        if directus.service in seen_services:
            raise LocalPlatformComposeError(f"Duplicate generated Directus service name: {directus.service}")
        if directus.public_port in seen_ports:
            raise LocalPlatformComposeError(f"Duplicate generated Directus public port: {directus.public_port}")
        seen_services.add(directus.service)
        seen_ports.add(directus.public_port)
        services.append(directus)
    return services


def directus_dependency_services_for_site(
    repo_root: Path,
    site_id: object,
    registry: LocalPlatformRegistry | None = None,
) -> list[GeneratedDirectusService]:
    clean_site_id = str(site_id or "").strip()
    if not clean_site_id:
        return []
    registry = registry or load_local_platform_registry(repo_root)
    site = registry.sites.get(clean_site_id)
    if site is None:
        return []
    directus = directus_service_for_site(repo_root, site, registry)
    return [directus] if directus is not None else []


def cms_dependency_service_names_for_site(repo_root: Path, site_id: object) -> list[str]:
    return [
        service.service
        for service in directus_dependency_services_for_site(repo_root, site_id)
        if service.service and service.managed
    ]


def _normalize_site_id_scope(site_ids: object) -> set[str] | None:
    if site_ids is None:
        return None
    if isinstance(site_ids, str):
        value = site_ids.strip()
        return {value} if value else set()
    try:
        return {str(value).strip() for value in site_ids if str(value).strip()}
    except TypeError:
        value = str(site_ids).strip()
        return {value} if value else set()


def generated_compose_services(
    repo_root: Path,
    registry: LocalPlatformRegistry | None = None,
    *,
    directus_site_ids: object = None,
    site_ids: object = None,
) -> list[GeneratedComposeService]:
    registry = registry or load_local_platform_registry(repo_root)
    directus_scope = _normalize_site_id_scope(directus_site_ids)
    site_scope = _normalize_site_id_scope(site_ids)
    services: list[GeneratedComposeService] = []
    seen_services: set[str] = set()
    for site in registry.list_sites():
        if site_scope is not None and site.id not in site_scope:
            continue
        metadata = load_runtime_site_metadata(repo_root, site)
        manifest = load_runtime_site_manifest(repo_root, site)
        directus = (
            directus_service_for_site(repo_root, site, registry)
            if directus_scope is None or site.id in directus_scope
            else None
        )
        ordered_lanes = [lane for lane in LANE_RENDER_ORDER if lane in site.lanes]
        ordered_lanes.extend(sorted(lane for lane in site.lanes if lane not in set(ordered_lanes)))
        for lane_name in ordered_lanes:
            lane = site.lanes[lane_name]
            service_name = validate_compose_service_name(lane.service)
            if service_name in seen_services:
                raise LocalPlatformComposeError(f"Duplicate generated Compose service name: {service_name}")
            seen_services.add(service_name)
            services.append(
                GeneratedComposeService(
                    site_id=site.id,
                    lane=lane_name,
                    service=service_name,
                    port=lane.port,
                    image=image_name_for_site_lane(site.id, lane_name),
                    site_name=metadata["name"],
                    site_kind=metadata["kind"],
                    site_lane=compose_environment_lane(lane_name),
                    directus_service=directus.service if directus else "",
                    directus_internal_url=directus.internal_url if directus else "",
                    directus_public_url=directus.public_url if directus else "",
                    directus_managed=bool(directus and directus.managed),
                    blog_enabled=bool(directus and _blog_runtime_enabled(manifest)),
                    blog_provider="directus" if directus and _blog_runtime_enabled(manifest) else "",
                    blog_content_runtime="directus" if directus and _blog_runtime_enabled(manifest) else "",
                    blog_collection=_directus_collection(manifest) if directus and _blog_runtime_enabled(manifest) else "",
                )
            )
    return services


def render_generated_websites_compose(
    repo_root: Path,
    registry: LocalPlatformRegistry | None = None,
    *,
    directus_site_ids: object = None,
    site_ids: object = None,
    compose_path: Path | None = None,
    file_header: str = GENERATED_FILE_HEADER,
) -> str:
    registry = registry or load_local_platform_registry(repo_root)
    services = generated_compose_services(
        repo_root,
        registry,
        directus_site_ids=directus_site_ids,
        site_ids=site_ids,
    )
    directus_services = generated_directus_services(
        repo_root,
        registry,
        directus_site_ids=directus_site_ids,
        site_ids=site_ids,
    )
    lines: list[str] = [
        file_header.rstrip(),
        f'name: "{compose_project_name()}"',
        "",
        "services:",
    ]
    if not services and not directus_services:
        lines.append("  {}")
        return "\n".join(lines) + "\n"
    for directus in directus_services:
        lines.extend(_render_directus_service(directus))
    for service in services:
        lines.extend(_render_service(service, repo_root, compose_path=compose_path))
    if directus_services:
        lines.extend(_render_directus_volumes(directus_services))
    return "\n".join(lines).rstrip() + "\n"


def render_generated_site_compose(
    repo_root: Path,
    site_id: object,
    registry: LocalPlatformRegistry | None = None,
) -> str:
    registry = registry or load_local_platform_registry(repo_root)
    site = _site_for_generated_compose(repo_root, site_id, registry)
    return render_generated_websites_compose(
        repo_root,
        registry,
        site_ids={site.id},
        compose_path=site_generated_compose_path(repo_root, site.id, registry),
        file_header=GENERATED_SITE_FILE_HEADER,
    )


def _write_generated_compose_file(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with tempfile.NamedTemporaryFile("w", encoding="utf-8", dir=path.parent, delete=False) as handle:
        handle.write(text)
        tmp_name = handle.name
    Path(tmp_name).replace(path)


def write_generated_websites_compose(
    repo_root: Path,
    registry: LocalPlatformRegistry | None = None,
    *,
    directus_site_ids: object = None,
) -> dict[str, Any]:
    text = render_generated_websites_compose(repo_root, registry, directus_site_ids=directus_site_ids)
    path = generated_compose_path(repo_root)
    _write_generated_compose_file(path, text)
    registry = registry or load_local_platform_registry(repo_root)
    services = generated_compose_services(repo_root, registry, directus_site_ids=directus_site_ids)
    directus_services = generated_directus_services(repo_root, registry, directus_site_ids=directus_site_ids)
    return {
        "ok": True,
        "path": str(path),
        "repo_relative_path": GENERATED_COMPOSE_RELATIVE_PATH.as_posix(),
        "service_count": len(services) + len(directus_services),
        "services": [service.service for service in directus_services] + [service.service for service in services],
        "cms_services": [service.service for service in directus_services],
    }


def write_generated_site_compose(
    repo_root: Path,
    site_id: object,
    registry: LocalPlatformRegistry | None = None,
) -> dict[str, Any]:
    registry = registry or load_local_platform_registry(repo_root)
    site = _site_for_generated_compose(repo_root, site_id, registry)
    path = site_generated_compose_path(repo_root, site.id, registry)
    text = render_generated_site_compose(repo_root, site.id, registry)
    _write_generated_compose_file(path, text)
    services = generated_compose_services(repo_root, registry, site_ids={site.id})
    directus_services = generated_directus_services(repo_root, registry, site_ids={site.id})
    return {
        "ok": True,
        "path": str(path),
        "repo_relative_path": site_generated_compose_relative_path(repo_root, site.id, registry).as_posix(),
        "site_id": site.id,
        "service_count": len(services) + len(directus_services),
        "services": [service.service for service in directus_services] + [service.service for service in services],
        "cms_services": [service.service for service in directus_services],
    }


def default_generated_websites_compose(repo_root: Path) -> str:
    registry = parse_local_platform_registry(repo_root, default_registry_data())
    return render_generated_websites_compose(repo_root, registry)


def _render_service(
    service: GeneratedComposeService,
    repo_root: Path,
    *,
    compose_path: Path | None = None,
) -> list[str]:
    lines = [
        f"  {service.service}:",
        "    build:",
        f"      context: {_yaml_string(site_server_build_context(repo_root, compose_path=compose_path))}",
        f"    image: {_yaml_string(service.image)}",
        "    restart: unless-stopped",
    ]
    if service.directus_service and service.directus_managed:
        lines.extend(
            [
                "    depends_on:",
                f"      - {_yaml_string(service.directus_service)}",
            ]
        )
    lines.extend(
        [
            "    environment:",
            f"      SITE_ID: {_yaml_string(service.site_id)}",
            f"      SITE_NAME: {_yaml_string(service.site_name)}",
            f"      SITE_KIND: {_yaml_string(service.site_kind)}",
            f"      SITE_LANE: {_yaml_string(service.site_lane)}",
            f"      MC_SITE_ID: {_yaml_string(service.site_id)}",
            f"      MC_RUNTIME_LANE: {_yaml_string(service.site_lane)}",
            f"      CONTENT_ROOT: {_yaml_string(CONTAINER_CONTENT_ROOT)}",
        ]
    )
    if service.directus_service:
        lines.extend(
            [
                f"      MC_DIRECTUS_SERVICE: {_yaml_string(service.directus_service)}",
                f"      DIRECTUS_URL: {_yaml_string(service.directus_internal_url)}",
                f"      DIRECTUS_PUBLIC_URL: {_yaml_string(service.directus_public_url)}",
            ]
        )
    if service.blog_enabled:
        lines.extend(
            [
                '      BLOG_ENABLED: "true"',
                f"      BLOG_PROVIDER: {_yaml_string(service.blog_provider or 'directus')}",
                f"      BLOG_CONTENT_RUNTIME: {_yaml_string(service.blog_content_runtime or 'directus')}",
                f"      BLOG_COLLECTION: {_yaml_string(service.blog_collection or 'posts')}",
            ]
        )
    lines.extend(
        [
            "    volumes:",
            f"      - {_yaml_string(websites_volume_mount(repo_root, compose_path=compose_path))}",
            "    ports:",
            f"      - {_yaml_string(f'0.0.0.0:{service.port}:8080')}",
            "",
        ]
    )
    return lines


def _render_directus_service(service: GeneratedDirectusService) -> list[str]:
    return [
        f"  {service.service}:",
        f"    image: {_yaml_string(service.image)}",
        "    restart: unless-stopped",
        "    environment:",
        f"      SECRET: {_yaml_string(service.secret_env)}",
        f"      ADMIN_EMAIL: {_yaml_string(service.admin_email_env)}",
        f"      ADMIN_PASSWORD: {_yaml_string(service.admin_password_env)}",
        '      DB_CLIENT: "sqlite3"',
        '      DB_FILENAME: "/directus/database/data.db"',
        '      STORAGE_LOCATIONS: "local"',
        '      STORAGE_LOCAL_ROOT: "/directus/uploads"',
        f"      PUBLIC_URL: {_yaml_string(service.public_url)}",
        "    volumes:",
        f"      - {_yaml_string(f'{service.database_volume}:/directus/database')}",
        f"      - {_yaml_string(f'{service.uploads_volume}:/directus/uploads')}",
        "    ports:",
        f"      - {_yaml_string(f'127.0.0.1:{service.public_port}:{DIRECTUS_INTERNAL_PORT}')}",
        "",
    ]


def _render_directus_volumes(services: list[GeneratedDirectusService]) -> list[str]:
    lines = ["volumes:"]
    seen: set[str] = set()
    for volume_name in [service.database_volume for service in services] + [service.uploads_volume for service in services]:
        if volume_name in seen:
            continue
        seen.add(volume_name)
        lines.extend(
            [
                f"  {volume_name}:",
                f"    name: {_yaml_string(volume_name)}",
            ]
        )
    return lines


def _yaml_string(value: object) -> str:
    return json.dumps(str(value), ensure_ascii=False)


def main(argv: list[str] | None = None) -> int:
    import argparse

    parser = argparse.ArgumentParser(description="Generate Docker Compose services for managed local websites.")
    parser.add_argument("--repo-root", default=".")
    parser.add_argument(
        "--check",
        action="store_true",
        help="Exit non-zero if the generated file is missing or stale, without writing it.",
    )
    args = parser.parse_args(argv)

    repo_root = Path(args.repo_root).resolve()
    rendered = render_generated_websites_compose(repo_root)
    path = generated_compose_path(repo_root)
    if args.check:
        current = path.read_text(encoding="utf-8") if path.exists() else ""
        if current != rendered:
            print(
                json.dumps(
                    {
                        "ok": False,
                        "stale": True,
                        "path": str(path),
                        "repo_relative_path": GENERATED_COMPOSE_RELATIVE_PATH.as_posix(),
                    },
                    indent=2,
                    sort_keys=True,
                )
            )
            return 1
        print(
            json.dumps(
                {
                    "ok": True,
                    "stale": False,
                    "path": str(path),
                    "repo_relative_path": GENERATED_COMPOSE_RELATIVE_PATH.as_posix(),
                },
                indent=2,
                sort_keys=True,
            )
        )
        return 0
    result = write_generated_websites_compose(repo_root)
    print(json.dumps(result, indent=2, sort_keys=True))
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
