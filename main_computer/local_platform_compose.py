def load_runtime_site_manifest(repo_root: Path, site) -> dict[str, Any]:
    manifest_path = repo_root / site.repo_relative_path / "site.json"
    try:
        payload = json.loads(manifest_path.read_text(encoding="utf-8"))
    except (FileNotFoundError, json.JSONDecodeError, OSError):
        return {}
    return payload if isinstance(payload, dict) else {}


def load_runtime_site_metadata(repo_root: Path, site) -> dict[str, str]:
    metadata = {
        "name": site.name,
        "kind": BUILTIN_SITE_KIND_OVERRIDES.get(site.id, site.kind),
    }
    payload = load_runtime_site_manifest(repo_root, site)
    name = str(payload.get("name") or "").strip()
    if name:
        metadata["name"] = name
    kind = str(payload.get("kind") or "").strip()
    if kind:
        metadata["kind"] = BUILTIN_SITE_KIND_OVERRIDES.get(site.id, kind)
    return metadata


def _cms_contract(payload: dict[str, Any]) -> dict[str, Any]:
    backend = payload.get("backend")
    if not isinstance(backend, dict):
        return {}
    cms = backend.get("cms")
    return cms if isinstance(cms, dict) else {}


def _blog_feature_contract(payload: dict[str, Any]) -> dict[str, Any]:
    features = payload.get("features")
    if not isinstance(features, dict):
        return {}
    blog = features.get("blog")
    return blog if isinstance(blog, dict) else {}


def _blog_requires_directus(payload: dict[str, Any]) -> bool:
    return _blog_runtime_enabled(payload) or _blog_directus_service_prep_requested(payload)


def _blog_directus_service_prep_requested(payload: dict[str, Any]) -> bool:
    """Return True only for an explicit Blog CMS-prep request.

    Blog intent alone must not materialize Directus.  During the Directus
    service-prep phase, Blog is still disabled, so this checks the narrow
    blog_install markers/layer state instead of features.blog.enabled.
    """

    blog = _blog_feature_contract(payload)
    if not isinstance(blog, dict) or blog.get("selected") is not True or blog.get("enabled") is True:
        return False
    if str(blog.get("cms") or "").strip().lower() != "directus":
        return False

    install = payload.get("blog_install")
    if not isinstance(install, dict):
        return False

    runtime_preparation = install.get("runtime_preparation")
    if isinstance(runtime_preparation, dict):
        marker = runtime_preparation.get("directus_service")
        if marker is True:
            return True
        if isinstance(marker, dict):
            status = str(marker.get("status") or "").strip().lower()
            if marker.get("requested") is True or status in {"pending_deploy", "deploying", "configured", "ready"}:
                return True

    layers = install.get("layers")
    cms_layer = layers.get("cms") if isinstance(layers, dict) else None
    if isinstance(cms_layer, dict):
        return str(cms_layer.get("status") or "").strip().lower() in {"configured", "deploying", "ready"}

    return False


def _blog_runtime_enabled(payload: dict[str, Any]) -> bool:
    features = payload.get("features")
    blog = _blog_feature_contract(payload)
    if not blog:
        return features.get("blog") is True if isinstance(features, dict) else False
    if blog.get("enabled") is not True:
        return False
    cms = str(blog.get("cms") or "").strip().lower()
    content = blog.get("content")
    provider = str(content.get("provider") or "").strip().lower() if isinstance(content, dict) else ""
    return cms == "directus" or provider == "directus"


def _directus_required(payload: dict[str, Any]) -> bool:
    blog = _blog_feature_contract(payload)
    if blog:
        return _blog_requires_directus(payload)
    cms = _cms_contract(payload)
    cms_required = cms.get("required") is True and str(cms.get("provider") or "").strip().lower() == "directus"
    return cms_required


def _directus_collection(payload: dict[str, Any]) -> str:
    cms = _cms_contract(payload)
    schema = cms.get("schema")
    if isinstance(schema, dict):
        collection = str(schema.get("collection") or "").strip()
        if collection:
            return collection
    blog = _blog_feature_contract(payload)
    content = blog.get("content")
    if isinstance(content, dict):
        collection = str(content.get("collection") or "").strip()
        if collection:
            return collection
    runtime_config = payload.get("runtime_config")
    runtime_content = runtime_config.get("content") if isinstance(runtime_config, dict) else None
    if isinstance(runtime_content, dict):
        collection = str(runtime_content.get("collection") or "").strip()
        if collection:
            return collection
    return "posts"


def _directus_local_connection_from_cms(cms: dict[str, Any]) -> dict[str, Any]:
    connection = cms.get("local_connection")
    return connection if isinstance(connection, dict) else {}


def _directus_local_connection_service_name(cms: dict[str, Any]) -> str:
    connection = _directus_local_connection_from_cms(cms)
    return str(connection.get("service_name") or "").strip()


def _directus_local_connection_volume(cms: dict[str, Any], key: str) -> str:
    connection = _directus_local_connection_from_cms(cms)
    return str(connection.get(key) or "").strip()


def _directus_local_connection_public_port(cms: dict[str, Any]) -> int | None:
    connection = _directus_local_connection_from_cms(cms)
    raw_port = connection.get("public_port")
    if raw_port in (None, ""):
        return None
    try:
        port = int(raw_port)
    except (TypeError, ValueError) as exc:
        raise LocalPlatformComposeError(f"Directus local_connection.public_port must be an integer TCP port: {raw_port!r}") from exc
    if port < 1 or port > 65535:
        raise LocalPlatformComposeError(f"Directus local_connection.public_port is outside 1-65535: {raw_port!r}")
    return port


def _directus_default_service_name(site_id: object) -> str:
    return validate_compose_service_name(f"{safe_image_slug(site_id)}-directus")


def _directus_local_connection_mode(cms: dict[str, Any]) -> str:
    connection = _directus_local_connection_from_cms(cms)
    return str(connection.get("mode") or "").strip().lower().replace("-", "_")


def _directus_local_connection_managed(site_id: object, cms: dict[str, Any], service_name: str) -> bool:
    connection = _directus_local_connection_from_cms(cms)
    if connection.get("managed") is False or connection.get("external") is True:
        return False
    mode = _directus_local_connection_mode(cms)
    if mode == "use_existing" and service_name != _directus_default_service_name(site_id):
        return False
    return True


def _ensure_directus_contract_for_blog(site_id: str, payload: dict[str, Any]) -> dict[str, Any]:
    """Backfill the Directus CMS contract for older Blog manifests.

    Earlier Blog installs could record the Blog feature intent without a complete
    backend.cms.required Directus contract. Compose generation still needs that
    contract so the generated site receives BLOG_* and DIRECTUS_* env vars.
    """

    if not _blog_requires_directus(payload):
        return payload

    backend = payload.get("backend")
    if not isinstance(backend, dict):
        backend = {}
        payload["backend"] = backend
    cms = backend.get("cms")
    if not isinstance(cms, dict):
        cms = {}
        backend["cms"] = cms

    cms["provider"] = "directus"
    cms["required"] = True
    cms["runtime"] = "deployed"
    cms.setdefault("database_connection", "content")

    service = cms.get("service")
    if not isinstance(service, dict):
        service = {}
        cms["service"] = service
    service.setdefault("kind", "directus")
    service.setdefault("image", "directus/directus:11.5.1")
    service.setdefault("internal_url", f"http://{safe_image_slug(site_id)}-directus:8055")
    service.setdefault("public_url", "")
    service.setdefault("admin_secret_ref", "directus_admin_token")

    storage = cms.get("storage")
    if not isinstance(storage, dict):
        storage = {}
        cms["storage"] = storage
    storage.setdefault("database_volume", f"{site_id}_directus_database")
    storage.setdefault("uploads_volume", f"{site_id}_directus_uploads")

    schema = cms.get("schema")
    if not isinstance(schema, dict):
        schema = {}
        cms["schema"] = schema
    schema.setdefault("collection", _directus_collection(payload))
    schema.setdefault("status", "pending_deploy")

    permissions = cms.get("permissions")
    if not isinstance(permissions, dict):
        permissions = {}
        cms["permissions"] = permissions
    permissions.setdefault("public_read_published_posts", True)
    permissions.setdefault("public_read_files", True)
    permissions.setdefault("status", "pending_deploy")

    cms.setdefault("schema_status", "pending_deploy")
    cms.setdefault("permissions_status", "pending_deploy")
    cms.setdefault("uploads_status", "pending_deploy")
    return payload


def _directus_service_host(site_id: str, cms: dict[str, Any]) -> str:
    connection_service = _directus_local_connection_service_name(cms)
    if connection_service:
        return validate_compose_service_name(connection_service)

    service = cms.get("service")
    raw_url = ""
    if isinstance(service, dict):
        raw_url = str(service.get("internal_url") or "").strip()
    if raw_url:
        try:
            hostname = str(urlsplit(raw_url).hostname or "").strip()
        except ValueError:
            hostname = ""
        if hostname:
            return validate_compose_service_name(hostname)
    return _directus_default_service_name(site_id)


def _directus_image(cms: dict[str, Any]) -> str:
    service = cms.get("service")
    if isinstance(service, dict):
        image = str(service.get("image") or "").strip()
        if image:
            return image
    return "directus/directus:11.5.1"


