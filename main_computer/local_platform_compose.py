def compose_project_name() -> str:
    return str(os.environ.get(ENV_COMPOSE_PROJECT) or COMPOSE_PROJECT_NAME).strip() or COMPOSE_PROJECT_NAME


def generated_compose_path(repo_root: Path) -> Path:
    override = str(os.environ.get(ENV_GENERATED_COMPOSE_PATH) or "").strip()
    if override:
        path = Path(override)
        if not path.is_absolute():
            path = repo_root / path
        return path.resolve()
    return repo_root / GENERATED_COMPOSE_RELATIVE_PATH


def _uses_default_generated_compose_path(repo_root: Path) -> bool:
    return generated_compose_path(repo_root).resolve() == (repo_root / GENERATED_COMPOSE_RELATIVE_PATH).resolve()


def _site_for_generated_compose(
    repo_root: Path,
    site_id: object,
    registry: LocalPlatformRegistry | None = None,
):
    registry = registry or load_local_platform_registry(repo_root)
    clean_site_id = str(site_id or "").strip().lower()
    if not clean_site_id:
        raise LocalPlatformComposeError("Missing website id for per-site Compose generation.")
    site = registry.sites.get(clean_site_id)
    if site is None:
        raise LocalPlatformComposeError(f"Website is not registered with the local platform: {clean_site_id}")
    return site


def site_generated_compose_relative_path(
    repo_root: Path,
    site_id: object,
    registry: LocalPlatformRegistry | None = None,
) -> PurePosixPath:
    site = _site_for_generated_compose(repo_root, site_id, registry)
    return PurePosixPath(site.repo_relative_path) / SITE_GENERATED_COMPOSE_RELATIVE_SUFFIX


def site_generated_compose_path(
    repo_root: Path,
    site_id: object,
    registry: LocalPlatformRegistry | None = None,
) -> Path:
    relative_path = site_generated_compose_relative_path(repo_root, site_id, registry)
    return repo_root.joinpath(*relative_path.parts)


def site_server_build_context(repo_root: Path, compose_path: Path | None = None) -> str:
    if compose_path is not None:
        return _compose_relative_host_path(compose_path, repo_root / "deploy" / "local-platform" / "site-server")
    if _uses_default_generated_compose_path(repo_root):
        return SITE_SERVER_BUILD_CONTEXT
    return _compose_host_path(repo_root / "deploy" / "local-platform" / "site-server")


def websites_volume_mount(repo_root: Path, compose_path: Path | None = None) -> str:
    if compose_path is not None:
        host_path = _compose_relative_host_path(compose_path, repo_root / "runtime" / "websites")
    elif _uses_default_generated_compose_path(repo_root):
        return WEBSITES_VOLUME
    else:
        host_path = _compose_host_path(repo_root / "runtime" / "websites")
    return f"{host_path}:/app/runtime/websites:ro"


def _compose_relative_host_path(compose_path: Path, target_path: Path) -> str:
    """Return a Compose host path relative to the given Compose file."""
    start = compose_path.resolve().parent
    target = target_path.resolve()
    return os.path.relpath(target, start).replace(os.sep, "/")


def _compose_host_path(path: Path) -> str:
    """Return a Docker Compose host path that is stable across Windows and POSIX."""
    return path.resolve().as_posix()


def safe_image_slug(value: object) -> str:
    slug = SAFE_SLUG_RE.sub("-", str(value or "").strip().lower()).strip("-")
    slug = re.sub(r"-{2,}", "-", slug)
    if not slug or not re.search(r"[a-z0-9]", slug):
        raise LocalPlatformComposeError(f"Cannot build a safe Compose image slug from: {value!r}")
    return slug


def validate_compose_service_name(value: object) -> str:
    service = str(value or "").strip()
    if not SERVICE_NAME_RE.fullmatch(service):
        raise LocalPlatformComposeError(f"Unsafe generated Compose service name: {service!r}")
    return service


